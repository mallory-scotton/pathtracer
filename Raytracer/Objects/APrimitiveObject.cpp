///////////////////////////////////////////////////////////////////////////////
APrimitiveObject::APrimitiveObject(const String& primitiveName)
    : AObject()
{
    m_name = primitiveName;
}

///////////////////////////////////////////////////////////////////////////////
void APrimitiveObject::BuildBVH(void)
{
    if (m_vertices.empty())
    {
        GenerateGeometry();
    }

    AObject::BuildBVH();
}

