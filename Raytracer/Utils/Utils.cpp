namespace Ray
{

///////////////////////////////////////////////////////////////////////////////
bool Utils::ToBoolean(const String& str)
{
    if (Utils::Equals(str, "true") || str == "1")
    {
        return (true);
    }
    return (false);
}

///////////////////////////////////////////////////////////////////////////////
float Utils::ToFloat(const String& str)
{
    return (std::stof(str));
}

///////////////////////////////////////////////////////////////////////////////
int Utils::ToInteger(const String& str)
{
    return (std::stoi(str));
}

///////////////////////////////////////////////////////////////////////////////
bool Utils::Equals(const String& a, const String& b)
{
    return (Utils::ToLower(a) == Utils::ToLower(b));
}

///////////////////////////////////////////////////////////////////////////////
String Utils::ToLower(const String& str)
{
    String temp = str;

    std::transform(temp.begin(), temp.end(), temp.begin(), ::tolower);

    return (temp);
}

///////////////////////////////////////////////////////////////////////////////
String Utils::ToUpper(const String& str)
{
    String temp = str;

    std::transform(temp.begin(), temp.end(), temp.begin(), ::toupper);

    return (temp);
}

} // namespace Ray
