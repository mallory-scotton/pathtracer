///////////////////////////////////////////////////////////////////////////////
// Namespace Ray
///////////////////////////////////////////////////////////////////////////////
namespace Ray
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class Scene
{
private:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    class Object
    {
    public:
        ///////////////////////////////////////////////////////////////////////
        //
        ///////////////////////////////////////////////////////////////////////
        enum class Type
        {
            RENDERER,
            CAMERA,
            MESH,
            LIGHT,
            MATERIAL,
            UNKNOWN
        };

    public:
        ///////////////////////////////////////////////////////////////////////
        //
        ///////////////////////////////////////////////////////////////////////
        Type type;                                  //<!
        Map<String, Vector<String>> properties;     //<!
    };

private:
    ///////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////
    Vector<Material> mMaterials;                //<!
    Map<String, Uint64> mMaterialMap;           //<!
    Vector<Light> mLights;                      //<!
    Vector<Index> mVertexIndices;               //<!
    Vector<Vec4f> mVerticesUVX;                 //<!
    Vector<Vec4f> mNormalsUVY;                  //<!
    Vector<Mat4x4f> mTransforms;                //<!
    Vector<UniquePtr<Texture>> mTextures;       //<!
    Vector<Byte> mTextureMapsArray;             //<!
    UniquePtr<Camera> mCamera;                  //<!
    UniquePtr<EnvMap> mEnvMap;                  //<!
    Renderer::Options mOptions;                 //<!

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param filePath
    ///
    ///////////////////////////////////////////////////////////////////////////
    Scene(const Path& filePath);

private:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param filePath
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    String ReadFile(const Path& filePath) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param filePath
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vector<Object> ParseObjectsFromFile(const Path& filePath) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param filePath
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool ParseSceneFile(const Path& filePath);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool IsUsingLights(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool IsUsingAlphaTest(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool IsUsingMedium(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Uint64 GetLightsCount(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool HasAnEnvironmentMap(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param filePath
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Uint64 AddTexture(const Path& filePath);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param materialTexture
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateMaterialTexture(GLuint* materialTexture) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param transformsTexture
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateTransformsTexture(GLuint* transformsTexture) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param vertexIndicesBuffer
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateVertexIndicesBuffer(
        GLuint* vertexIndicesBuffer, GLuint* vertexIndicesTexture
    ) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param verticesBuffer
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateVerticesBuffer(
        GLuint* verticesBuffer,
        GLuint* verticesTexture
    ) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param normalsBuffer;
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateNormalsBuffer(
        GLuint* normalsBuffer,
        GLuint* normalsTexture
    ) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param lightsTexture
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateLightsTexture(GLuint* lightsTexture) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param textureMapsArrayTexture
    ///
    ///////////////////////////////////////////////////////////////////////////
    void CreateTextureMapsArrayTexture(
        GLuint* textureMapsArrayTexture,
        const Renderer::Options& options
    ) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param shader
    ///
    ///////////////////////////////////////////////////////////////////////////
    void UpdateUniforms(UniquePtr<Shader>& shader) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Renderer::Options GetRendererOptions(void) const;
};

} // namespace Ray
