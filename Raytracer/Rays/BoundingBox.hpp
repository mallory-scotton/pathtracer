///////////////////////////////////////////////////////////////////////////////
// Header guard
///////////////////////////////////////////////////////////////////////////////
#pragma once

///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include "Config.hpp"
#include "Maths/Vec3.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace Ray
///////////////////////////////////////////////////////////////////////////////
namespace Ray
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class BoundingBox
{
private:
    ///////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////
    Vec3f mMin;         //<!
    Vec3f mMax;         //<!

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    BoundingBox(void);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    ///////////////////////////////////////////////////////////////////////////
    BoundingBox(const Vec3f& point);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point1
    /// \param point2
    ///
    ///////////////////////////////////////////////////////////////////////////
    BoundingBox(const Vec3f& point1, const Vec3f& point2);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param index
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    const Vec3f& operator[](Uint64 index) const;

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static BoundingBox Union(const BoundingBox& a, const BoundingBox& b);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static BoundingBox Intersection(
        const BoundingBox& a,
        const BoundingBox& b
    );

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    /// \param destination
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static void Intersection(
        const BoundingBox& a,
        const BoundingBox& b,
        BoundingBox& destination
    );

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static bool Intersects(const BoundingBox& a, const BoundingBox& b);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static bool Contains(const BoundingBox& a, const BoundingBox& b);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f Center(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f Extents(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool Contains(const Vec3f& point) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param box
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool Contains(const BoundingBox& box) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    int MaxDimension(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    float SurfaceArea(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Grow(const Vec3f& point);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param box
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Grow(const BoundingBox& box);
};

} // namespace Ray
