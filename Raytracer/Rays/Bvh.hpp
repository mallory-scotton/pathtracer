///////////////////////////////////////////////////////////////////////////////
// Header guard
///////////////////////////////////////////////////////////////////////////////
#pragma once

///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include "Rays/BBox.hpp"
#include "Config.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace Ray
///////////////////////////////////////////////////////////////////////////////
namespace Ray
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class Bvh
{
private:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    class Node
    {
    public:
        ///////////////////////////////////////////////////////////////////////
        /// \brief
        ///
        ///////////////////////////////////////////////////////////////////////
        enum Type
        {
            INTERNAL,
            LEAF
        };

    private:
        ///////////////////////////////////////////////////////////////////////
        //
        ///////////////////////////////////////////////////////////////////////
        BBox mBounds;               //<!
        Type mType;                 //<!
        int mIndex;                 //<!
        union
        {
            struct
            {
                Node* mLeft;        //<!
                Node* mRight;       //<!
            };

            struct
            {
                int mStartIndex;    //<!
                int mNumPrims;      //<!
            };
        };
    };

private:
    ///////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////
    Vector<Bvh::Node> mNodes;       //<!
    Vector<int> mIndices;           //<!
    Atomic<int> mNodeCount;         //<!
    Vector<int> mPackedIndices;     //<!
    BBox mBounds;                   //<!
    Node* mRoot;                    //<!
    bool mUseSAH;                   //<!
    int mHeight;                    //<!
    float mTraversalCost;           //<!
    int mNumBins;                   //<!

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param traversalCost
    /// \param numBins
    /// \param useSAH
    ///
    ///////////////////////////////////////////////////////////////////////////
    Bvh(float traversalCost, int numBins = 64, bool useSAH = false);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    virtual ~Bvh() = default;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    Bvh(const Bvh&) = delete;

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    Bvh& operator=(const Bvh&) = delete;

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    const BBox& GetBounds(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    int GetHeight(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    const Vector<int>& GetIndices(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    const int* GetIndicesData(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Uint64 GetIndicesCount(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param bounds
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Build(const Vector<BBox>& bounds);
};

} // namespace Ray
