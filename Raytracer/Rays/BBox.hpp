///////////////////////////////////////////////////////////////////////////////
// Header guard
///////////////////////////////////////////////////////////////////////////////
#pragma once

///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include "Config.hpp"
#include "Maths/Vec3.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace Ray
///////////////////////////////////////////////////////////////////////////////
namespace Ray
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class BBox
{
private:
    ///////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////
    Vec3f mMin;         //<!
    Vec3f mMax;         //<!

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    BBox(void);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    ///////////////////////////////////////////////////////////////////////////
    BBox(const Vec3f& point);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point1
    /// \param point2
    ///
    ///////////////////////////////////////////////////////////////////////////
    BBox(const Vec3f& point1, const Vec3f& point2);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param index
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    const Vec3f& operator[](Uint64 index) const;

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static BBox Union(const BBox& a, const BBox& b);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static BBox Intersection(
        const BBox& a,
        const BBox& b
    );

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    /// \param destination
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static void Intersection(
        const BBox& a,
        const BBox& b,
        BBox& destination
    );

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static bool Intersects(const BBox& a, const BBox& b);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param a
    /// \param b
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    static bool Contains(const BBox& a, const BBox& b);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f Center(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f Extents(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool Contains(const Vec3f& point) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param box
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    bool Contains(const BBox& box) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    int MaxDimension(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    float SurfaceArea(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param point
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Grow(const Vec3f& point);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param box
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Grow(const BBox& box);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f GetMin(void) const;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Vec3f GetMax(void) const;
};

} // namespace Ray
